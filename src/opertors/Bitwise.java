package opertors;

public class Bitwise {
	public static void main(String[] args) {
		System.out.println(12&10);//8421
		System.out.println(12|10);//1100=12
		System.out.println(12^10);//1010=10
		System.out.println(~10);
		System.out.println(~-10);
		System.out.println(15<<3);
		System.out.println(-15<<3);
		System.out.println(15>>3);
		System.out.println(-531>>3);
	}                             
}
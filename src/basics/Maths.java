package basics;

public class Maths {
	public static void main(String[] args) {
		System.out.println((int)Math.pow(2, 7));// power = 2power7
		System.out.println((int)Math.ceil(2.3));//upper
		System.out.println((int)Math.floor(2.8));//lower
		System.out.println(Math.round(2.5));// round off
		System.out.println(Math.round(2.4));
		System.out.println((int)Math.sqrt(100)+" value of square");
		System.out.println(Math.abs(-25));// -ve to +ve
		System.out.println(Math.abs(25));
		System.out.println(Math.max(2 ,5));// max value
		System.out.println(Math.min(2 ,5));//min value
	}

}
